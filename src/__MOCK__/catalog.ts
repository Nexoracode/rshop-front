export const addParentToCategores = (
  inputCategories: Category[],
  parent: Category | null
): Category[] => {
  return inputCategories.map((i) => {
    const addedParent = {
      ...i,
      parent: parent
        ? ({
            id: parent.id,
            title: parent.title,
            slug: parent.slug,
            level: parent?.level,
            parent: parent.parent,
            parent_id: parent.id,
            discount: parent.discount,
          } as Category)
        : null,
      parent_id: parent?.id ?? 0,
    };

    return {
      ...addedParent,
      children: addParentToCategores(i.children ?? [], addedParent),
    };
  });
};

