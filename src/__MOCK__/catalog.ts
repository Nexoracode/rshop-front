const findCategory = (
  category_id: number,
  categories: Category[]
): Partial<Category> | null => {
  const result = categories.map((c) => {
    const { children, ...cc } = c;

    return c.id === category_id
      ? cc
      : findCategory(category_id, children ?? []);
  });

  return result.find((i) => i) ?? null;
};
